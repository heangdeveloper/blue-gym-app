import type { Metadata } from "next";
import "./css/globals.css";
import { NuqsAdapter } from "nuqs/adapters/next/app";
import {NextIntlClientProvider} from 'next-intl';
import { Inter, Battambang } from "next/font/google";
import { cookies } from "next/headers";
import { ConfirmDialogProvider } from "./provider/ConfirmDialogProvider";

const inter = Inter({
    subsets: ['latin'],
    display: 'swap',
})

const battambang = Battambang({
    subsets: ["khmer"],
    weight: ["400", "700"],
    display: "swap",
});

export const metadata: Metadata = {
    title: "Flow | Blue Gym",
    description: "Generated by create next app",
};

export default async function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    const cookieStore = await cookies();
    const locale = cookieStore.get("locale")?.value || "kh";
    const fontClass = locale === "kh" ? battambang.className : inter.className;
    return (
        <html
            lang={locale}
            className={fontClass}
        >
            <body>
                <NextIntlClientProvider locale={locale}>
                    <NuqsAdapter>
                        <ConfirmDialogProvider>
                            {children}
                        </ConfirmDialogProvider>
                    </NuqsAdapter>
                </NextIntlClientProvider>
            </body>
        </html>
    );
}
